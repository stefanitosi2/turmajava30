package programas;

public class Animal {

	
		private String nome;
		private int emiteSom;
		
		
		public Animal() {
			super();
			this.nome = nome;
			this.emiteSom = emiteSom;
		}


		public String getNome() {
			return nome;
		}


		public void setNome(String nome) {
			this.nome = nome;
		}


		public int getEmiteSom() {
			return emiteSom;
		}


		public void setEmiteSom(int emiteSom) {
			this.emiteSom = emiteSom;
		}
		
		
		
		

	

}
